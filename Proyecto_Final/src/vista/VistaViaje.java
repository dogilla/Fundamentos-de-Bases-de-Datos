    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //origen y destino
        this.rv.add(jTextField1.getText());
        this.rv.add(jTextField2.getText());
        //idcliennte
        this.rv.add(jTextField3.getText()); 
        //numero de personas
        this.rv.add(jTextField4.getText());
        int distancia = ThreadLocalRandom.current().nextInt(1, 10 + 1);
        String dis = Integer.toString(distancia);
        String tiempo = this.modelo.getTiempo();
        int cuota;
        double ganancia;
        if(rsi.isSelected()){
            cuota = 15 + (distancia*10);
            ganancia = cuota * 0.08;
        }else{
            cuota = 15 + (distancia*8);
            ganancia = cuota * 0.12;
        }
        try {
            this.modelo.EntradaViaje(dis, tiempo, rv.get(3), rv.get(0), rv.get(1), rv.get(2), 
                                    Integer.toString(count),Double.toString(ganancia));
            count = count +1; 
        } catch (SQLException ex) {
            Logger.getLogger(VistaViaje.class.getName()).log(Level.SEVERE, null, ex);
        }
        String message;
        message = "El costo de tu viaje es de: " + Integer.toString((cuota))+ "\n"
                   + "y la ganancia del conductor es de: " + Double.toString(ganancia);
        JOptionPane.showMessageDialog(null, message);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

